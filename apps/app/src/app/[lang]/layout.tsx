// Packages
import type { Metadata } from "next"
import { Geist, Inter, Open_Sans, Poppins } from "next/font/google"
// Dictionaries
import { DEFAULT_LANGUAGE, Locale } from "@/dictionaries/dictionaries"
// Actions
import {
  getOrganizationKeyFromCookies,
  getOrganizationTheme
} from "@/actions/organization"
// Context
import { DEFAULT_ACCENT_THEME, DEFAULT_FONT_THEME, ThemeProvider } from "@/context/ThemeContext"
// Components
import OrganizationKeyHandler from "@/components/common/OrganizationKeyHandler/OrganizationKeyHandler"
// Types
import { FontTheme } from "@/actions/organization"
// Styles
import "../globals.css"

interface RootLayoutProps {
  children: React.ReactNode
  params: Promise<{ lang: string }>
}

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
}

const geist = Geist({
  subsets: ["latin"],
})
const openSans = Open_Sans({
  subsets: ["latin"],
})
const inter = Inter({
  subsets: ["latin"],
})
const poppins = Poppins({ subsets: ["latin"], weight: ["400", "500", "600", "700"] })

const FONTS_CLASSNAMES: Record<FontTheme, string> = {
  geist: geist.className,
  "open-sans": openSans.className,
  inter: inter.className,
  poppins: poppins.className,
}

export default async function RootLayout({
  children,
  params,
}: RootLayoutProps) {
  const { lang: langParam = DEFAULT_LANGUAGE } = await params
  // Validate and ensure lang is a valid Locale
  const lang: Locale = (langParam === "en" || langParam === "fr") ? langParam : DEFAULT_LANGUAGE

  const organizationKey = await getOrganizationKeyFromCookies()
  const { accentColor, font } = (await getOrganizationTheme(organizationKey)) ?? { accentColor: DEFAULT_ACCENT_THEME, font: DEFAULT_FONT_THEME }
  const fontClassName = FONTS_CLASSNAMES[font]

  return (
    <html
      lang={lang}
      className={`${fontClassName} antialiased`}
      data-accent-theme={accentColor}
      data-font-theme={font}
    >
      <body className="flex flex-col items-center justify-start w-full lg:h-svh bg-background">
        <OrganizationKeyHandler />
        <ThemeProvider
          initialTheme={accentColor}
          initialFont={font}
          fontClassNames={FONTS_CLASSNAMES}
        >
          {children}
        </ThemeProvider>
      </body>
    </html>
  )
}
